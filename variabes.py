# a first look at variables

# python is dynamically typed (you can put any type of data into any variable)
myVar = 51
print(myVar)

# when you put new data into a variable, it overwrites the old data
myVar = "Hello!"
print(myVar)

# variables are case-sensitive
myvar = 51
print(myVar)

# you can do math with numeric variables
print(myvar-10)

